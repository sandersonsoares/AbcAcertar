    private void letra2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_letra2ActionPerformed
        if (lSorteada1 == certa) {
            Som.acertou.play();
            this.score++;
            JOptionPane.showMessageDialog(null, "Voce Acertou!");
            panelJogar.removeAll();
            sortear();
            initComponents();
            cl = (CardLayout) panelMenu.getLayout();
            cl.show(panelMenu, "jogar");
            this.certa = sortearCerta();
        } else {
            Som.errou.play();
            vida--;
            JOptionPane.showMessageDialog(null, "Voce Errou!");
            if (vida <= 0) {
                Main.run(score);
                dispose();
            } else {
                panelJogar.removeAll();
                sortear();
                initComponents();
                cl = (CardLayout) panelMenu.getLayout();
                cl.show(panelMenu, "jogar");
                this.certa = sortearCerta();
            }
        }
    }//GEN-LAST:event_letra2ActionPerformed

