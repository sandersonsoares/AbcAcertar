    public void sortear() {
        List<Letra> alfabeto = letrasDAO.listarAlfabeto();
        int sorteado = rd.nextInt(alfabeto.size());
        lSorteada = letrasDAO.buscarId(sorteado);

        sorteado = rd.nextInt(alfabeto.size());
        lSorteada1 = letrasDAO.buscarId(sorteado);

        sorteado = rd.nextInt(alfabeto.size());
        lSorteada2 = letrasDAO.buscarId(sorteado);

        if (lSorteada == lSorteada1 || lSorteada == lSorteada2 || lSorteada1 == lSorteada2) {
            sortear();
        }
        if (lSorteada == null || lSorteada1 == null || lSorteada2 == null) {
            sortear();
        }
    }

    public Letra sortearCerta() {
        final int opSort = rd.nextInt(3);
        String letra = "letra" + opSort;
        Som.qual.play();
        final List<Letra> lts = new ArrayList<>();
        lts.add(lSorteada);
        lts.add(lSorteada1);
        lts.add(lSorteada2);
        try {
            new Thread() {
                public void run() {
                    try {
                        td.sleep(1500L);
                        lts.get(opSort).getSom().play();
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Inicial.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }.start();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return lts.get(opSort);
    }

